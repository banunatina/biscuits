var expect = require('chai').expect;
var sudokuSolver = require('../prompts/sudokuSolver/solution');

describe('sudoku solver', function (done) {
  it('should solve this board', function (done) {
    var board = [
      [ 8, 7, ' ', ' ', ' ', ' ', ' ', 9, ' ' ],
      [ ' ', ' ', 5, ' ', ' ', 9, ' ', 4, ' ' ],
      [ ' ', ' ', ' ', 7, 5, ' ', ' ', 8, ' ' ],
      [ 4, ' ', ' ', 9, 1, ' ', ' ', ' ', ' ' ],
      [ ' ', ' ', ' ', 5, ' ', 4, 9, ' ', ' ' ],
      [ ' ', 3, 8, ' ', ' ', ' ', ' ', ' ', ' ' ],
      [ ' ', 2, ' ', ' ', 7, ' ', ' ', ' ', ' ' ],
      [ ' ', ' ', ' ', 4, ' ', ' ', 2, ' ', ' ' ],
      [ ' ', ' ', 4, ' ', 9, ' ', ' ', 1, 7 ]];
    var solution = [
      [ 8, 7, 1, 2, 4, 6, 3, 9, 5 ],
      [ 2, 6, 5, 8, 3, 9, 7, 4, 1 ],
      [ 3, 4, 9, 7, 5, 1, 6, 8, 2 ],
      [ 4, 5, 2, 9, 1, 3, 8, 7, 6 ],
      [ 7, 1, 6, 5, 8, 4, 9, 2, 3 ],
      [ 9, 3, 8, 6, 2, 7, 1, 5, 4 ],
      [ 5, 2, 3, 1, 7, 8, 4, 6, 9 ],
      [ 1, 9, 7, 4, 6, 5, 2, 3, 8 ],
      [ 6, 8, 4, 3, 9, 2, 5, 1, 7 ] ];
    expect(sudokuSolver(board)).to.deep.equal(solution);
  });
  it('should solve this other board', function (done) {
    var board = [
      [ ' ', 7, ' ', ' ', ' ', ' ', ' ', ' ', 3 ],
      [ 3, ' ', ' ', 2, ' ', 9, 1, ' ', 5 ],
      [ 5, ' ', ' ', ' ', 6, ' ', ' ', 2, ' ' ],
      [ ' ', ' ', 6, ' ', 4, ' ', ' ', 3, ' ' ],
      [ ' ', ' ', ' ', 7, ' ', ' ', 4, ' ', ' ' ],
      [ 1, ' ', ' ', ' ', ' ', 2, ' ', ' ', ' ' ],
      [ ' ', 1, ' ', 4, ' ', ' ', ' ', ' ', ' ' ],
      [ 7, ' ', ' ', 6, ' ', ' ', ' ', 8, ' ' ],
      [ ' ', ' ', ' ', 5, 8, ' ', ' ', ' ', 2 ] ];
    var solution = [
      [ 6, 7, 2, 8, 1, 5, 9, 4, 3 ],
      [ 3, 8, 4, 2, 7, 9, 1, 6, 5 ],
      [ 5, 9, 1, 3, 6, 4, 7, 2, 8 ],
      [ 9, 5, 6, 1, 4, 8, 2, 3, 7 ],
      [ 2, 3, 8, 7, 5, 6, 4, 9, 1 ],
      [ 1, 4, 7, 9, 3, 2, 8, 5, 6 ],
      [ 8, 1, 5, 4, 2, 3, 6, 7, 9 ],
      [ 7, 2, 3, 6, 9, 1, 5, 8, 4 ],
      [ 4, 6, 9, 5, 8, 7, 3, 1, 2 ] ];
    expect(sudokuSolver(board)).to.deep.equal(solution);
  });
});
